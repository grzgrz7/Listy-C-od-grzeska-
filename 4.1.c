#include <stdio.h>
#include <stdlib.h>
#include <stdbool.h>

// Napisz program, który wczytuje ze standardowego wejścia trzy liczby
// całkowite, a następnie wypisuje je w oddzielnych liniach na standardowym wyjściu.

int main()
{

    int x, y, z;

    scanf("%d", &x);
    scanf("%d", &y);
    scanf("%d", &z);

    printf("%d\n", x);
    printf("%d\n", y);
    printf("%d\n", z);

    return 0;
}

